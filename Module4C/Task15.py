# Declare a list with any value and number of item/element
# Print the list in console
# Sort the list
# Print the list in console again

perfume = ['rabanne', 'gucci', 'hugo boss', 'yves saint laurent', 'dior']
print('List of perfumes:', perfume)

# ascending order
perfume.sort()
print('Sort-asc:', perfume)

# descending order
perfume.sort(reverse=True)
print('Sort-desc:', perfume)